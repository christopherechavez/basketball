    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        String CourtID = cid.getText().trim();

        if (CourtID.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please select a court to delete!", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(null, 
            "Are you sure you want to delete this court?", 
            "Confirm Delete", 
            JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            connectDB connect = new connectDB();
            try {
                // Check if court exists
                if (!connect.fieldExists("courts", "c_id", CourtID)) {
                    JOptionPane.showMessageDialog(null, "Court ID does not exist!", "Error", JOptionPane.WARNING_MESSAGE);
                    return;
                }

//                // Check if court has any reservations
//                if (connect.fieldExists("reservations", "c_id", CourtID)) {
//                    JOptionPane.showMessageDialog(null, "Cannot delete court with existing reservations!", "Error", JOptionPane.WARNING_MESSAGE);
//                    return;
//                }

                String sql = "DELETE FROM courts WHERE c_id = ?";
                int rowsDeleted = connect.deleteData(sql, CourtID);

                if (rowsDeleted > 0) {
                    JOptionPane.showMessageDialog(null, "Court deleted successfully!");
                    new Court().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to delete court!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_deleteMouseClicked

