    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
      if (cname.getText().isEmpty() || address.getText().isEmpty() || 
    cc.getSelectedItem() == null || ct.getSelectedItem() == null || 
    status.getSelectedItem() == null || cid.getText().isEmpty()) {

    JOptionPane.showMessageDialog(null, "All Fields Are Required!", "Error", JOptionPane.WARNING_MESSAGE);

} else {
    connectDB cdb = new connectDB();
    try {
        int courtId;
        try {
            courtId = Integer.parseInt(cid.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid Court ID!", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Check if court name already exists (excluding current court)
        boolean courtNameExists = cdb.duplicateCheckExcludingCurrent("courts", "cname", cname.getText(), "c_id", String.valueOf(courtId));

        if (courtNameExists) {
            JOptionPane.showMessageDialog(null, "Court name already exists for another court!", "Validation Error", JOptionPane.WARNING_MESSAGE);
        } else {
            int rowsUpdated = cdb.updateData(
                "UPDATE courts SET cname = ?, caddress = ?, ccapacity = ?, ctype = ?, status = ? WHERE c_id = ?",
                cname.getText().trim(),
                address.getText().trim(),
                cc.getSelectedItem().toString(),
                ct.getSelectedItem().toString(),
                status.getSelectedItem().toString(),
                courtId
            );

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Court updated successfully!");
                this.dispose();  
                // Open whatever window should come next after update
                new Court().setVisible(true); this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Update failed! Court ID not found.", "Update Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Database Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } finally {
        cdb.closeConnection();
    }
}
    }//GEN-LAST:event_updateMouseClicked

